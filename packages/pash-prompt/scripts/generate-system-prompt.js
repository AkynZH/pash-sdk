#!/usr/bin/env node
'use strict';

/**
 * Генерирует SYSTEM_PROMPT.md из текущего состояния реестра.
 *
 * Использование:
 *   node scripts/generate-system-prompt.js
 *   node scripts/generate-system-prompt.js --stdout
 */

const fs   = require('fs');
const path = require('path');
const { PromptEngine } = require('../index');
const { SCHEMAS }      = require('pash-sdk');

const CONFIGS = [
  { lang: 'ru', mode: 'pash',    title: 'RU / mode: pash'    },
  { lang: 'ru', mode: 'pash+id', title: 'RU / mode: pash+id' },
  { lang: 'ru', mode: 'events',  title: 'RU / mode: events'  },
  { lang: 'en', mode: 'pash',    title: 'EN / mode: pash'    },
  { lang: 'en', mode: 'pash+id', title: 'EN / mode: pash+id' },
  { lang: 'en', mode: 'events',  title: 'EN / mode: events'  },
];

const engine = new PromptEngine({ schemas: SCHEMAS });

const lines = [
  '# PASH System Prompt Reference',
  '',
  '> Auto-generated by @pash/prompt.',
  '> Run: `node node_modules/@pash/prompt/scripts/generate-system-prompt.js`',
  '',
  'Copy the block matching your language and mode into your LLM system role.',
  '',
  '---',
  '',
];

for (const cfg of CONFIGS) {
  const prompt = engine.generate({ lang: cfg.lang, mode: cfg.mode });
  lines.push(`## ${cfg.title}`);
  lines.push('');
  lines.push('```');
  lines.push(prompt);
  lines.push('```');
  lines.push('');
  lines.push('---');
  lines.push('');
}

const output = lines.join('\n');

if (process.argv.includes('--stdout')) {
  process.stdout.write(output);
} else {
  const dest = path.resolve(process.cwd(), 'SYSTEM_PROMPT.md');
  fs.writeFileSync(dest, output, 'utf8');
  console.log(`✓ SYSTEM_PROMPT.md updated (${CONFIGS.length} variants)`);
  console.log(`  Path: ${dest}`);
}
